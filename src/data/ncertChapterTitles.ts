export function getChapterTitle(lang: Language, bookCode: string, chapterNumber: number): string {
  const list = lang === 'hi' ? TITLES_HI[bookCode] ?? TITLES_EN[bookCode] : TITLES_EN[bookCode];
  if (list && chapterNumber >= 1 && chapterNumber <= list.length) {
    return list[chapterNumber - 1];
  }
  return lang === 'hi' ? `अध्याय ${chapterNumber}` : `Chapter ${chapterNumber}`;
}
